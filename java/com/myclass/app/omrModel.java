package com.myclass.app;

public class omrModel {
    int num;
    String ans;

    public omrModel(int num, String ans) {
        this.num = num;
        this.ans = ans;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getAns() {
        return ans;
    }

    public void setAns(String ans) {
        this.ans = ans;
    }
}
